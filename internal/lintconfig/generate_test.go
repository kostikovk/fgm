func TestGenerate_StandardPreset(t *testing.T) {
	t.Parallel()

	catalog, err := LoadCatalog()
	if err != nil {
		t.Fatalf("LoadCatalog: %v", err)
	}

	data, err := Generate(catalog, GenerateOptions{
		GoVersion:   "1.26.1",
		LintVersion: "v2.11.2",
		Preset:      PresetStandard,
	})
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	output := string(data)
	if !strings.Contains(output, "version: \"2\"") {
		t.Fatal("expected version: \"2\" in output")
	}
	if !strings.Contains(output, "govet") {
		t.Fatal("expected govet in output")
	}
	if !strings.Contains(output, "default: none") {
		t.Fatal("expected default: none in output")
	}
	if strings.Contains(output, "formatters") {
		t.Fatal("formatters section should not be present without --with-imports")
	}
}

